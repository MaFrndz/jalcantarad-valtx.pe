package com.finnegansfact.finnegans_chile.Entities;

public class Direcciones {
    private String PaisCodigo; 
    private String ProvinciaCodigo; 
    private String LocalidadCodigo; 
    private String CodigoPostal; 
    private String Calle; 
    private String Numero; 
    private String Dpto; 
    private String Piso; 
    private Integer Tipo;
    private String Descripcion; 
    private Boolean Principal;
    public String getPaisCodigo() {
        return PaisCodigo;
    }
    public void setPaisCodigo(String paisCodigo) {
        PaisCodigo = paisCodigo;
    }
    public String getProvinciaCodigo() {
        return ProvinciaCodigo;
    }
    public void setProvinciaCodigo(String provinciaCodigo) {
        ProvinciaCodigo = provinciaCodigo;
    }
    public String getLocalidadCodigo() {
        return LocalidadCodigo;
    }
    public void setLocalidadCodigo(String localidadCodigo) {
        LocalidadCodigo = localidadCodigo;
    }
    public String getCodigoPostal() {
        return CodigoPostal;
    }
    public void setCodigoPostal(String codigoPostal) {
        CodigoPostal = codigoPostal;
    }
    public String getCalle() {
        return Calle;
    }
    public void setCalle(String calle) {
        Calle = calle;
    }
    public String getNumero() {
        return Numero;
    }
    public void setNumero(String numero) {
        Numero = numero;
    }
    public String getDpto() {
        return Dpto;
    }
    public void setDpto(String dpto) {
        Dpto = dpto;
    }
    public String getPiso() {
        return Piso;
    }
    public void setPiso(String piso) {
        Piso = piso;
    }
    public Integer getTipo() {
        return Tipo;
    }
    public void setTipo(Integer tipo) {
        Tipo = tipo;
    }
    public String getDescripcion() {
        return Descripcion;
    }
    public void setDescripcion(String descripcion) {
        Descripcion = descripcion;
    }
    public Boolean getPrincipal() {
        return Principal;
    }
    public void setPrincipal(Boolean principal) {
        Principal = principal;
    }  
    
    
}
